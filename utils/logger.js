const winston = require('winston');
const path = require('path');

// Define log levels
const levels = {
  error: 0,
  warn: 1,
  info: 2,
  http: 3,
  debug: 4,
};

// Define colors for each level
const colors = {
  error: 'red',
  warn: 'yellow',
  info: 'green',
  http: 'magenta',
  debug: 'white',
};

// Tell winston that you want to link the colors
winston.addColors(colors);

// Define which level to log based on environment
const level = () => {
  const env = process.env.NODE_ENV || 'development';
  const isDevelopment = env === 'development';
  return isDevelopment ? 'debug' : 'warn';
};

// Define format for logs
const format = winston.format.combine(
  winston.format.timestamp({ format: 'YYYY-MM-DD HH:mm:ss:ms' }),
  winston.format.colorize({ all: true }),
  winston.format.printf(
    (info) => `${info.timestamp} ${info.level}: ${info.message}`,
  ),
);

// Define transports
const transports = [
  // Console transport
  new winston.transports.Console({
    format: winston.format.combine(
      winston.format.colorize(),
      winston.format.simple()
    )
  }),
  
  // File transport for errors
  new winston.transports.File({
    filename: path.join(__dirname, '../logs/error.log'),
    level: 'error',
    format: winston.format.combine(
      winston.format.timestamp(),
      winston.format.json()
    )
  }),
  
  // File transport for all logs
  new winston.transports.File({
    filename: path.join(__dirname, '../logs/combined.log'),
    format: winston.format.combine(
      winston.format.timestamp(),
      winston.format.json()
    )
  }),
];

// Create the logger
const logger = winston.createLogger({
  level: level(),
  levels,
  format,
  transports,
  exitOnError: false,
});

// Create a stream object for Morgan HTTP logging
logger.stream = {
  write: (message) => {
    logger.http(message.trim());
  },
};

// Add request logging middleware
logger.logRequest = (req, res, next) => {
  const start = Date.now();
  
  res.on('finish', () => {
    const duration = Date.now() - start;
    logger.info(`${req.method} ${req.originalUrl} ${res.statusCode} ${duration}ms`);
  });
  
  next();
};

// Add error logging middleware
logger.logError = (error, req, res, next) => {
  logger.error(`${error.message} - ${req.originalUrl} - ${req.method} - ${req.ip}`);
  next(error);
};

// Add security event logging
logger.logSecurityEvent = (event, details) => {
  logger.warn(`SECURITY EVENT: ${event} - ${JSON.stringify(details)}`);
};

// Add threat detection logging
logger.logThreatDetection = (threat, details) => {
  logger.error(`THREAT DETECTED: ${threat.type} - ${JSON.stringify(details)}`);
};

// Add compliance logging
logger.logComplianceEvent = (event, details) => {
  logger.info(`COMPLIANCE EVENT: ${event} - ${JSON.stringify(details)}`);
};

// Add insurance logging
logger.logInsuranceEvent = (event, details) => {
  logger.info(`INSURANCE EVENT: ${event} - ${JSON.stringify(details)}`);
};

module.exports = logger; 